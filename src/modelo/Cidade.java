package modelo;

/**
 *
 * @author Aluno
 */
public class Cidade {
    private Integer id;
    private String nome;
    private String uf;

    /**
     * @return the id
     */
    
    public Cidade(){}
    
    public Integer getId() {
        return id;
    }

    
    /**
     * @param id the id to set
     */
    public void setId(Integer id) {
        this.id = id;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the uf
     */
    public String getUf() {
        return uf;
    }

    /**
     * @param uf the uf to set
     */
    public void setUf(String uf) {
        this.uf = uf;
    }
    
    
}
