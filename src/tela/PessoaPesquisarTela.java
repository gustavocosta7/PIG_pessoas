    private void carregarTabela() {
        try {
            
            Pessoa p = new Pessoa();
            p.setNome(campoTextoPesquisar.getText());
            pessoaControle.setPessoa(p);
            pessoaControle.selecionar();
            pessoas = pessoaControle.getPessoas();
            PessoaTableModel modelo = new PessoaTableModel(pessoas);
            tabelaPessoas.setModel(modelo);
        } catch(SQLException | DAOException ex){
            System.out.println(ex.getMessage() + "erro de carregar tabela");
        }

    }

